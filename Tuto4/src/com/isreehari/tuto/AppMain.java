    public static void main(String[] args) {
        // TODO code application logic here
        
        int myVar = 100;
        System.out.println(myVar);
        
        int anotherVar = 200;
        
        System.out.println(anotherVar);
        
        // prefix Increment opperator
        System.out.println(++anotherVar);
        
        // postfix Increment opperator
        System.out.println(anotherVar++);
        
        // prefix decrement opperator
        System.out.println(--anotherVar);
        
        // postfix decrement opperator
        System.out.println(anotherVar--);
        
        //Compound assignment operator 
       int compountVal = 10;
       compountVal += 10;
       
       System.out.println(compountVal);
       
        
        
        
        
    }
    
